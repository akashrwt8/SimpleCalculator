#include <stdio.h>

// Addition

int add (int a, int b) {
    return a + b;
}

int add (int a, int b, int c) {
    return a + b + c;
}

float add (float a, float b) {
    return a + b;
}
// Subtraction 

int sub (int a, int b) {
    return a + b;
}

float sub (float a, float b) {
    return a + b;
}
